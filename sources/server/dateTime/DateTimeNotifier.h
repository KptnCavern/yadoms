#pragma once
#include <Poco/Util/Timer.h>
#include <Poco/DateTime.h>
#include <Poco/Timespan.h>

namespace dateTime {

   //-----------------------------------------------------------------------------
   /// \class   Class which start a date time notifier (send notification every minute)
   //-----------------------------------------------------------------------------
   class CDateTimeNotifier
   {
   public:
      //-----------------------------------------------------------------------------
      /// \brief		      Constructor
      //-----------------------------------------------------------------------------
      CDateTimeNotifier();

      //-----------------------------------------------------------------------------
      /// \brief		                     Destructor
      //-----------------------------------------------------------------------------
      virtual ~CDateTimeNotifier();

      //-----------------------------------------------------------------------------
      /// \brief		                     Start the date/time notification service
      //-----------------------------------------------------------------------------
      void start();

      //-----------------------------------------------------------------------------
      /// \brief		                     Stop the date/time notification service
      //-----------------------------------------------------------------------------
      void stop();

      //-----------------------------------------------------------------------------
      /// \brief		                     Task runner
      //-----------------------------------------------------------------------------
      void taskRun(Poco::Util::TimerTask& task);

   private:
      //-----------------------------------------------------------------------------
      /// \brief		                     Schedule the next occurence
      //-----------------------------------------------------------------------------
      void scheduleOnNextMinute();


      //--------------------------------------------------------------
      /// \Brief		The datetime task
      //--------------------------------------------------------------
      Poco::Util::TimerTask::Ptr m_dateTimeTask;

      //--------------------------------------------------------------
      /// \Brief		The datetime timer
      //--------------------------------------------------------------
      boost::shared_ptr<Poco::Util::Timer> m_dateTimeTimer;

      //--------------------------------------------------------------
      /// \Brief		The next minute to wait
      //--------------------------------------------------------------
      Poco::DateTime m_nextMinute;

      //--------------------------------------------------------------
      /// \Brief		One minute offset
      //--------------------------------------------------------------
      static Poco::Timespan m_oneMinuteOffset;

   };

} //namespace dateTime
